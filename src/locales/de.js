    let company_singular = translations ? translations[`singular-${moduleIds?.company}-de`] : '';
    let company_plural = translations ? translations[`plural-${moduleIds?.company}-de`] : '';
    let email_temp_singular = translations ? translations[`singular-${moduleIds?.email_temp}-de`] : '';
